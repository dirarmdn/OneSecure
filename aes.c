void aes_shift_rows(AES_State *state) {
  uint8_t temp;

  // Row 1 is not shifted
  // Row 2 is shifted 1 byte to the left
  temp = state->state[0][1];
  state->state[0][1] = state->state[1][1];
  state->state[1][1] = state->state[2][1];
  state->state[2][1] = state->state[3][1];
  state->state[3][1] = temp;

  // Row 3 is shifted 2 bytes to the left
  temp = state->state[0][2];
  state->state[0][2] = state->state[2][2];
  state->state[2][2] = temp;
  temp = state->state[1][2];
  state->state[1][2] = state->state[3][2];
  state->state[3][2] = temp;

  // Row 4 is shifted 3 bytes to the left
  temp = state->state[0][3];
  state->state[0][3] = state->state[3][3];
  state->state[3][3] = state->state[2][3];
  state->state[2][3] = state->state[1][3];
  state->state[1][3] = temp;
}

uint8_t aes_mul(uint8_t a, uint8_t b) {
    uint8_t result = 0;
    uint8_t high_bit_set;
    for (int i = 0; i < 8; i++) {
        if ((b & 1) == 1) {
            result ^= a;
        }
        high_bit_set = a & 0x80;
        a <<= 1;
        if (high_bit_set == 0x80) {
            a ^= 0x1b; // XOR with 0x1b if high bit was set before shifting
        }
        b >>= 1;
    }
    return result;
}


void aes_mix_columns(AES_State *state) {
  uint8_t temp, a, b;

  for (int col = 0; col < 4; col++) {
    a = state->state[0][col];
    b = state->state[1][col];
    // Mix the first two elements
    temp = a << 1;
    temp ^= (a & 0x80) >> 7;  // Implement x^8 over GF(2^8)
    state->state[0][col] = b ^ temp ^ a;
    state->state[1][col] = a ^ b ^ temp;

    // Mix the remaining two elements
    a = state->state[2][col];
    b = state->state[3][col];
    temp = a << 1;
    temp ^= (a & 0x80) >> 7;  // Implement x^8 over GF(2^8)
    state->state[2][col] = b ^ temp ^ a;
    state->state[3][col] = a ^ b ^ temp;
  }
}

void aes_decrypt(AES_State *state, const AES_Key *key) {
    aes_add_round_key(state, (AES_Key*)key + NR * 4);  // Initial round key addition

    for (int round = NR - 1; round >= 1; round--) {
        aes_inv_shift_rows(state);
        aes_inv_sub_bytes(state);
        aes_add_round_key(state, (AES_Key*)key + round * 4);
        aes_inv_mix_columns(state);
    }

    aes_inv_shift_rows(state);
    aes_inv_sub_bytes(state);
    aes_add_round_key(state, key);  // Final round key addition

    // Remove padding
    int padding_count = 0;
    for (int i = 0; i < 16; i++) {
        if (state->state[0][i] == ' ') {
            padding_count++;
        }
    }
    state->state[0][16 - padding_count] = '\0'; // Null-terminate at the end of the plaintext
}


void aes_inv_shift_rows(AES_State *state) {
  uint8_t temp;

  // Row 1 is not shifted
  // Row 2 is shifted 1 byte to the right
  temp = state->state[3][1];
  state->state[3][1] = state->state[2][1];
  state->state[2][1] = state->state[1][1];
  state->state[1][1] = state->state[0][1];
  state->state[0][1] = temp;

  // Row 3 is shifted 2 bytes to the right
  temp = state->state[0][2];
  state->state[0][2] = state->state[2][2];
  state->state[2][2] = temp;
  temp = state->state[1][2];
  state->state[1][2] = state->state[3][2];
  state->state[3][2] = temp;

  // Row 4 is shifted 3 bytes to the right
  temp = state->state[0][3];
  state->state[0][3] = state->state[1][3];
  state->state[1][3] = state->state[2][3];
  state->state[2][3] = state->state[3][3];
  state->state[3][3] = temp;
}

