// KeyExpansion Function
void KeyExpansion(unsigned char *key, AESKey *roundKeys, int keySize) {
    int i, j;
    unsigned char temp[4], k;
    int rounds = 0;

    if (keySize == AES_128_KEY_SIZE)
        rounds = 10;
    else if (keySize == AES_192_KEY_SIZE)
        rounds = 12;
    else if (keySize == AES_256_KEY_SIZE)
        rounds = 14;

    // The first round key is the original key
    memcpy(roundKeys[0].data, key, keySize);
    roundKeys[0].size = keySize;

    // The remaining round keys are derived from the previous round keys
    for (i = 1; i <= rounds; ++i) {
        // Copy the previous round key into temp
        memcpy(temp, &roundKeys[i - 1].data[keySize - 4], 4);

        // Perform the key schedule core (rotate, S-box, and Rcon)
        if (i % (keySize / 4) == 0) {
            // Rotate temp by 1 byte
            k = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = k;

            // Apply S-box to each byte of temp
            for (j = 0; j < 4; ++j)
                temp[j] = sbox[temp[j]];

            // XOR the first byte of temp with the corresponding Rcon value
            temp[0] ^= Rcon[i / (keySize / 4)];
        }

        // XOR temp with the round key four bytes earlier
        for (j = 0; j < 4; ++j)
            roundKeys[i].data[j] = roundKeys[i - 1].data[j] ^ temp[j];
        for (j = 4; j < keySize; ++j)
            roundKeys[i].data[j] = roundKeys[i - 1].data[j] ^ roundKeys[i].data[j - 4];

        roundKeys[i].size = keySize;
    }
}


// ExpandRoundKey Function
void ExpandRoundKey(unsigned char *key, unsigned char *roundKey, int round) {
    int i, j;

    // Copy the round key from the key schedule into roundKey
    for (i = 0; i < AES_BLOCK_SIZE; ++i) {
        roundKey[i] = key[round * AES_BLOCK_SIZE + i];
    }

    // Apply the S-box to each byte of the round key
    for (i = 0; i < AES_BLOCK_SIZE; ++i) {
        roundKey[i] = sbox[roundKey[i]];
    }

    // XOR the round key with the key schedule of the previous round
    for (i = 0; i < AES_BLOCK_SIZE; ++i) {
        roundKey[i] ^= key[(round - 1) * AES_BLOCK_SIZE + i];
    }

    // XOR the round key with the first byte of the Rcon value for the current round
    roundKey[0] ^= Rcon[round];
}


