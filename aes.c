void aes_inv_mix_columns(AES_State *state) {
  uint8_t temp, a, b, c, d;

  for (int col = 0; col < 4; col++) {
    a = state->state[0][col];
    b = state->state[1][col];
    c = state->state[2][col];
    d = state->state[3][col];

    state->state[0][col] = aes_mul(a, 0x0e) ^ aes_mul(b, 0x0b) ^ aes_mul(c, 0x0d) ^ aes_mul(d, 0x09);
    state->state[1][col] = aes_mul(a, 0x09) ^ aes_mul(b, 0x0e) ^ aes_mul(c, 0x0b) ^ aes_mul(d, 0x0d);
    state->state[2][col] = aes_mul(a, 0x0d) ^ aes_mul(b, 0x09) ^ aes_mul(c, 0x0e) ^ aes_mul(d, 0x0b);
    state->state[3][col] = aes_mul(a, 0x0b) ^ aes_mul(b, 0x0d) ^ aes_mul(c, 0x09) ^ aes_mul(d, 0x0e);
  }
}

void aes_encrypt(AES_State *state, const AES_Key *key) {
  // Round 0: Initial round
  aes_add_round_key(state, key);

  // Main rounds
  for (int round = 1; round < NR; round++) {
    aes_sub_bytes(state);
    aes_shift_rows(state);
    aes_mix_columns(state);
    aes_add_round_key(state, (AES_Key*)key + round * 4);  // Access round key
  }

  // Final round
  aes_sub_bytes(state);
  aes_shift_rows(state);
  aes_add_round_key(state, (AES_Key*)key + NR * 4);  // Access last round key
}
