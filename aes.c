void ShiftRows(unsigned char state[AES_BLOCK_SIZE]) {
    unsigned char temp;

    // Row 1 tidak bergeser

    // Row 2 bergeser 1 byte ke kiri
    temp = state[1];
    state[1] = state[5];
    state[5] = state[9];
    state[9] = state[13];
    state[13] = temp;

    // Row 3 bergeser 2 byte ke kiri
    temp = state[2];
    state[2] = state[10];
    state[10] = temp;
    temp = state[6];
    state[6] = state[14];
    state[14] = temp;

    // Row 4 bergeser 3 byte ke kiri
    temp = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7] = state[3];
    state[3] = temp;
}

// MixColumns Function
void MixColumns(unsigned char state[AES_BLOCK_SIZE]) {
		int i;
    unsigned temp[AES_BLOCK_SIZE];

    for (i = 0; i < 4; ++i) {
        temp[4 * i + 0] = (unsigned char)(mul2[state[4 * i + 0]] ^ mul3[state[4 * i + 1]] ^ state[4 * i + 2] ^ state[4 * i + 3]);
        temp[4 * i + 1] = (unsigned char)(state[4 * i + 0] ^ mul2[state[4 * i + 1]] ^ mul3[state[4 * i + 2]] ^ state[4 * i + 3]);
        temp[4 * i + 2] = (unsigned char)(state[4 * i + 0] ^ state[4 * i + 1] ^ mul2[state[4 * i + 2]] ^ mul3[state[4 * i + 3]]);
        temp[4 * i + 3] = (unsigned char)(mul3[state[4 * i + 0]] ^ state[4 * i + 1] ^ state[4 * i + 2] ^ mul2[state[4 * i + 3]]);
    }
    
    // Copy temp back to state
    for (i = 0; i < AES_BLOCK_SIZE; ++i) {
        state[i] = temp[i];
    }
}

// Initialization Function
void aes_init(AESContext *ctx, unsigned char *key, int key_size, unsigned char *iv, AES_MODE mode) {
    // Set the encryption key
    KeyExpansion(key, &ctx->key, key_size);

    // Set the initialization vector (IV)
    if (iv != NULL) {
        memcpy(ctx->iv, iv, AES_BLOCK_SIZE);
    } else {
        // If IV is not provided, set it to all zeros
        memset(ctx->iv, 0, AES_BLOCK_SIZE);
    }

    // Set the mode of operation
    ctx->mode = mode;
}

// Encryption Function
void aes_encrypt(AESContext *ctx, unsigned char *input, unsigned char *output, int length) {
    int i, j;
    unsigned char roundKey[AES_BLOCK_SIZE];
    unsigned char state[AES_BLOCK_SIZE];

    // Loop through the input data by block
    for (i = 0; i < length; i += AES_BLOCK_SIZE) {
        // Copy the block of data into the state
        memcpy(state, input + i, AES_BLOCK_SIZE);

        // Initial round: AddRoundKey
        AddRoundKey(state, ctx->key.data);

        // Rounds
        for (j = 1; j < ctx->key.size / 4; ++j) {
            SubBytes(state);
            ShiftRows(state);
            MixColumns(state);
            ExpandRoundKey(ctx->key.data, roundKey, j);
            AddRoundKey(state, roundKey);
        }

        // Final round (no MixColumns)
        SubBytes(state);
        ShiftRows(state);
        ExpandRoundKey(ctx->key.data, roundKey, j);
        AddRoundKey(state, roundKey);

        // Copy the state to the output buffer
        memcpy(output + i, state, AES_BLOCK_SIZE);
    }
}

// Decryption Function
void aes_decrypt(AESContext *ctx, unsigned char *input, unsigned char *output, int length) {
    int i, j;
    unsigned char roundKey[AES_BLOCK_SIZE];
    unsigned char state[AES_BLOCK_SIZE];

    // Loop through the input data by block
    for (i = 0; i < length; i += AES_BLOCK_SIZE) {
        // Copy the block of data into the state
        memcpy(state, input + i, AES_BLOCK_SIZE);

        // Initial round: AddRoundKey
        ExpandRoundKey(ctx->key.data, roundKey, ctx->key.size / 4 - 1);
        AddRoundKey(state, roundKey);

        // Rounds (in reverse order)
        for (j = ctx->key.size / 4 - 2; j > 0; --j) {
            InvShiftRows(state);
            InvSubBytes(state);
            ExpandRoundKey(ctx->key.data, roundKey, j);
            AddRoundKey(state, roundKey);
            InvMixColumns(state);
        }

        // Final round (no InvMixColumns)
        InvShiftRows(state);
        InvSubBytes(state);
        ExpandRoundKey(ctx->key.data, roundKey, 0);
        AddRoundKey(state, roundKey);

        // Copy the state to the output buffer
        memcpy(output + i, state, AES_BLOCK_SIZE);
    }
}
